let currentLang = 'en';
let currentFilter = 'all';

function T(key) { return (i18n[currentLang] && i18n[currentLang][key]) || i18n.en[key] || key; }
function TObj(obj) { return (typeof obj === 'object') ? (obj[currentLang] || obj.en || '') : obj; }

