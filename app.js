function renderCards() {
  const grid = document.getElementById('cardsGrid');
  const searchEl = document.getElementById('detailSearchInput');
  const search = searchEl ? searchEl.value.toLowerCase() : '';
  const filtered = resources.filter(r => {
    const matchCat = currentFilter === 'all' || r.category === currentFilter;
    const text = [TObj(r.title), TObj(r.desc), TObj(r.who), TObj(r.what), TObj(r.how)].join(' ').toLowerCase();
    return matchCat && (!search || text.includes(search));
  });

  grid.innerHTML = filtered.map(r => {
    const staleDays = daysSince(r.updated);
    const downRatio = r.notHelpful / (r.helpful + r.notHelpful);
    const needsUpdate = downRatio > 0.15 || staleDays > 180;
    return `
      <div class="card" data-cat="${r.category}">
        <span class="card-category ${r.catClass}">${TObj(r.catLabel)}</span>
        ${needsUpdate ? `<div class="update-banner">${T('update_warning')} <strong>${T('last_verified')} ${fmtDate(r.updated)}</strong></div>` : ''}
        <h3>${TObj(r.title)}</h3>
        <p class="card-desc">${TObj(r.desc)}</p>
        <div class="w5h1">
          <div class="w5h1-item"><span class="w5h1-label">${T('w_who')}</span><span class="w5h1-value">${TObj(r.who)}</span></div>
          <div class="w5h1-item"><span class="w5h1-label">${T('w_what')}</span><span class="w5h1-value">${TObj(r.what)}</span></div>
          <div class="w5h1-item"><span class="w5h1-label">${T('w_when')}</span><span class="w5h1-value">${TObj(r.when)}</span></div>
          <div class="w5h1-item"><span class="w5h1-label">${T('w_where')}</span><span class="w5h1-value">${TObj(r.where)}</span></div>
          <div class="w5h1-item"><span class="w5h1-label">${T('w_why')}</span><span class="w5h1-value">${TObj(r.why)}</span></div>
          <div class="w5h1-item"><span class="w5h1-label">${T('w_how')}</span><span class="w5h1-value">${TObj(r.how)}</span></div>
        </div>
        <div class="card-links">${r.links.map(l=>`<a class="card-link" href="${l.url}" target="_blank">${l.label} ↗</a>`).join('')}</div>
        <div class="card-footer">
          <div class="vote-row">
            <button class="vote-btn" onclick="vote(${r.id},'up',this)">👍 ${T('btn_helpful')} <strong>${r.helpful}</strong></button>
            <button class="vote-btn" onclick="vote(${r.id},'down',this)">👎 ${T('btn_outdated')} <strong>${r.notHelpful}</strong></button>
          </div>
          <span class="card-updated">${T('updated_label')} <span class="${staleDays>180?'stale':'fresh'}">${fmtDate(r.updated)}</span></span>
        </div>
      </div>`;
  }).join('');

  if (!filtered.length) {
    grid.innerHTML = `<div style="grid-column:1/-1;text-align:center;padding:3rem;color:var(--text-tertiary)">
      <p style="font-size:1.2rem;margin-bottom:0.5rem">${T('no_results')}</p>
      <p style="font-size:0.9rem">${T('no_results_hint')}</p></div>`;
  }
}

function renderForum() {
  document.getElementById('forumPosts').innerHTML = forumPosts.map(p => {
    const replyCount = p.replies ? p.replies.length : 0;
    const previewCount = 2;
    const hasMore = replyCount > previewCount;
    return `
    <div class="forum-post">
      <div class="forum-header">
        <div class="forum-avatar">${p.avatar}</div>
        <div class="forum-meta"><span class="forum-user">${p.user}</span><span class="forum-time">${TObj(p.time)}</span></div>
        <span class="forum-reply-count">💬 ${replyCount} ${T('reply_count')}</span>
      </div>
      <div class="forum-body">${TObj(p.body)}</div>
      <div class="forum-tags">${p.tags.map(t=>`<span class="forum-tag">#${t}</span>`).join('')}</div>
      ${replyCount > 0 ? `
        <div class="replies-section" id="replies-${p.id}">
          <button class="replies-toggle" onclick="toggleReplies(${p.id})">
            <span class="replies-toggle-icon" id="replies-icon-${p.id}">▶</span>
            <span id="replies-label-${p.id}">${T('show_replies').replace('{n}', replyCount)}</span>
          </button>
          <div class="replies-list" id="replies-list-${p.id}" style="display:none">
            ${p.replies.map((r, i) => `
              <div class="reply ${i >= previewCount ? 'reply-extra reply-extra-' + p.id : ''}" ${i >= previewCount && hasMore ? 'style="display:none"' : ''}>
                <div class="reply-line"></div>
                <div class="reply-content">
                  <div class="reply-header">
                    <div class="reply-avatar">${r.avatar}</div>
                    <span class="reply-user">${r.user}</span>
                    <span class="reply-time">${TObj(r.time)}</span>
                  </div>
                  <div class="reply-body">${TObj(r.body)}</div>
                </div>
              </div>
            `).join('')}
            ${hasMore ? `
              <button class="show-more-btn" id="more-btn-${p.id}" onclick="showMoreReplies(${p.id})">
                ${T('show_more_replies').replace('{n}', replyCount - previewCount)}
              </button>
            ` : ''}
          </div>
        </div>
      ` : ''}
    </div>`;
  }).join('');
}

function toggleReplies(postId) {
  const list = document.getElementById('replies-list-' + postId);
  const icon = document.getElementById('replies-icon-' + postId);
  const label = document.getElementById('replies-label-' + postId);
  const post = forumPosts.find(p => p.id === postId);
  const count = post ? post.replies.length : 0;
  const isOpen = list.style.display !== 'none';
  list.style.display = isOpen ? 'none' : 'block';
  icon.textContent = isOpen ? '▶' : '▼';
  label.textContent = isOpen
    ? T('show_replies').replace('{n}', count)
    : T('hide_replies');
}

function showMoreReplies(postId) {
  document.querySelectorAll('.reply-extra-' + postId).forEach(el => {
    el.style.display = 'flex';
  });
  const btn = document.getElementById('more-btn-' + postId);
  if (btn) btn.style.display = 'none';
}

function renderChecklist() {
  const container = document.getElementById('checklistContainer');
  const cats = [...new Set(checklistData.map(c => c.category))];
  const done = checklistData.filter(c => c.done).length;
  const pct = Math.round((done / checklistData.length) * 100);

  container.innerHTML = `<div class="checklist-card">
    <div class="progress-label">${done} ${T('completed_of')} ${checklistData.length} ${T('completed_suffix')} (${pct}%)</div>
    <div class="progress-bar"><div class="progress-fill" style="width:${pct}%"></div></div>
    ${cats.map(cat => `
      <h4 style="font-size:0.78rem;font-weight:600;color:var(--text-tertiary);text-transform:uppercase;letter-spacing:0.06em;margin:1rem 0 0.35rem;padding-top:0.5rem;border-top:1px solid var(--border-light)">${T(cat)}</h4>
      ${checklistData.filter(c=>c.category===cat).map(item=>`
        <div class="checklist-item" onclick="toggleCheck(${item.id})">
          <div class="check-box ${item.done?'checked':''}"></div>
          <span class="checklist-text ${item.done?'done':''}">${TObj(item.text)}</span>
        </div>
      `).join('')}
    `).join('')}
  </div>`;
}

