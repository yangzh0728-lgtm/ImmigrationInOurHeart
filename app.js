function vote(id, dir, btn) {
  const r = resources.find(x => x.id === id);
  if (!r) return;
  if (dir==='up') { r.helpful++; btn.classList.add('voted-up'); }
  else { r.notHelpful++; btn.classList.add('voted-down'); }
  btn.innerHTML = dir==='up'
    ? `👍 ${T('btn_helpful')} <strong>${r.helpful}</strong>`
    : `👎 ${T('btn_outdated')} <strong>${r.notHelpful}</strong>`;
  if (r.notHelpful/(r.helpful+r.notHelpful) > 0.15) renderCards();
}
