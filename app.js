function toggleLangDropdown() {
  const btn = document.getElementById('langBtn');
  const dd = document.getElementById('langDropdown');
  const isOpen = dd.classList.contains('open');
  dd.classList.toggle('open');
  btn.classList.toggle('open');
  if (!isOpen) {
    document.addEventListener('click', closeLangDropdown);
  }
}
function closeLangDropdown(e) {
  const wrap = document.querySelector('.lang-switcher-wrap');
  if (!wrap.contains(e.target)) {
    document.getElementById('langDropdown').classList.remove('open');
    document.getElementById('langBtn').classList.remove('open');
    document.removeEventListener('click', closeLangDropdown);
  }
}

function switchLang(lang) {
  currentLang = lang;
  // Update body font class
  document.body.className = lang !== 'en' ? `lang-${lang}` : '';
  // Update html lang
  document.documentElement.lang = lang === 'zh-CN' ? 'zh-Hans' : lang === 'zh-TW' ? 'zh-Hant' : 'en';

  // Update button label
  const labels = { en: 'English', 'zh-CN': '简体中文', 'zh-TW': '繁體中文' };
  document.getElementById('langBtnLabel').textContent = labels[lang];

  // Update dropdown active state
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });

  // Close dropdown
  document.getElementById('langDropdown').classList.remove('open');
  document.getElementById('langBtn').classList.remove('open');

  // Apply translations to all static [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[lang] && i18n[lang][key]) {
      el.textContent = i18n[lang][key];
    } else if (i18n.en[key]) {
      el.textContent = i18n.en[key];
    }
  });

  // Apply placeholder translations
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = T(key);
  });

  // Re-render dynamic content
  renderHomepage();
  renderCards();
  renderForum();
  renderChecklist();
}

